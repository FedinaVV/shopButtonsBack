package com.fedina.shop;

public class LittleClass {
    private int x = 1;
    private int y = 2;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void doubleXX() {
        x = x*2;
    }

    public int getDoubleXX () {
        return x*2;
    }

    public int getTriple(int q){
        return q*3;
    }
}
